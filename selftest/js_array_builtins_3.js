/*
Exercise 3: Flattening Nested Arrays

Write a function that takes an array of nested arrays and returns a new array with all the elements flattened.

Use the `flat` method to solve this problem.

Example:
Input: [[1, 2], [3, 4], [5, 6]]
Output: [1, 2, 3, 4, 5, 6]
*/

function flattenArray(arr) {
    // Write your code here
    return arr.flat()
} 